
package ventasmunicipio;

public class Municipios {
    private String municipioA;
    private String municipioB;
    private String municipioC;
    private int totalA; 
    private int totalB; 
    private int totalC; 

    /**
     * @return the municipioA
     */
    public String getMunicipioA() {
        return municipioA;
    }

    /**
     * @param municipioA the municipioA to set
     */
    public void setMunicipioA(String municipioA) {
        this.municipioA = municipioA;
    }

    /**
     * @return the municipioB
     */
    public String getMunicipioB() {
        return municipioB;
    }

    /**
     * @param municipioB the municipioB to set
     */
    public void setMunicipioB(String municipioB) {
        this.municipioB = municipioB;
    }

    /**
     * @return the municipioC
     */
    public String getMunicipioC() {
        return municipioC;
    }

    /**
     * @param municipioC the municipioC to set
     */
    public void setMunicipioC(String municipioC) {
        this.municipioC = municipioC;
    }

    /**
     * @return the totalA
     */
    public int getTotalA() {
        return totalA;
    }

    /**
     * @param totalA the totalA to set
     */
    public void setTotalA(int totalA) {
        this.totalA = totalA;
    }

    /**
     * @return the totalB
     */
    public int getTotalB() {
        return totalB;
    }

    /**
     * @param totalB the totalB to set
     */
    public void setTotalB(int totalB) {
        this.totalB = totalB;
    }

    /**
     * @return the totalC
     */
    public int getTotalC() {
        return totalC;
    }

    /**
     * @param totalC the totalC to set
     */
    public void setTotalC(int totalC) {
        this.totalC = totalC;
    }
    
}
